n1 = int(input("Digite o primeiro valor: "))
n2 = int(input("Digite o segundo valor: "))
# também se pode criar variáveis para receber os valores das operações
print("Esse é o resultado da SOMA: {}".format(n1+n2))
print("Esse é o resultado da SUBTRAÇÃO: {}".format(n1-n2))
print("Esse é o resultado da MULTIPLICAÇÃO: {}".format(n1*n2))
print("Esse é o resultado da DIVISÃO: {}".format(n1/n2))